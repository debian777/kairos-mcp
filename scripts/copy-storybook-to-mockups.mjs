#!/usr/bin/env node
/**
 * Copy built Storybook (storybook-static) to docs/design/mockups/static
 * so stakeholders can open the same screens as the app without running Storybook.
 * Run after: npm run storybook:build
 */
import { cpSync, existsSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const root = join(__dirname, "..");
const source = join(root, "storybook-static");
const target = join(root, "docs", "design", "mockups", "static");

if (!existsSync(source)) {
  console.error("Run npm run storybook:build first.");
  process.exit(1);
}

rmSync(target, { recursive: true, force: true });
mkdirSync(target, { recursive: true });
cpSync(source, target, { recursive: true });

const readme = `# Static Storybook export (1:1 mockups)

**Generated by:** \`npm run storybook:export-mockups\`
**Do not edit by hand.** Regenerate after UI changes.

Open \`index.html\` in a browser to view the same screens as the app (Home, KAIROS, Account, Protocol detail, etc.). Navigate via the sidebar: **Screens** → story name.

This is the static build of Storybook; the source of truth is React components and stories in \`src/ui/**/*.stories.tsx\`.
`;
writeFileSync(join(target, "README.md"), readme);
console.log("Copied storybook-static to docs/design/mockups/static");
